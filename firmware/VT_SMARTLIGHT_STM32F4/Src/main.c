int fputc( int iChar, FILE *pxNotUsed )
{ 
		if(osSemaphoreWait(putc_sem_id, 100) == osOK)
		{
			mUART5_PutChar(iChar);
		}
		osSemaphoreRelease(putc_sem_id);
  	return( iChar );
}

int  m_printf(const uint8_t *format, ...)
{
	if(osSemaphoreWait(mprintf_sem_id, 500) == osOK)
	{
		static  uint8_t  buffer[512];
		uint16_t len = 0,i;
		__va_list     vArgs;
		__va_start(vArgs, format);
		len = sprintf((char *)buffer,"\n\rUTC:%04d/%02d/%02d %02d:%02d:%02d #",sysTime.year,sysTime.month,sysTime.mday,sysTime.hour,sysTime.min,sysTime.sec);
		len += vsprintf((char *)&buffer[len], (char const *)format, vArgs);
		__va_end(vArgs);
		for(i = 0;i < len; i++)
		{
				fputc(buffer[i],NULL);
		}
	}
	osSemaphoreRelease(mprintf_sem_id);
	return 0;
}

